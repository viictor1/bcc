"""9. Escreva um programa que imprime a tabuada de um número informado, em duas colunas"""

num = int(input("Digite um número para descobrir a sua tabuada "))

print(f"{num} * 1 = {num * 1:<5} {num} * 6 = {num * 6}")
print(f"{num} * 2 = {num * 2:<5} {num} * 7 = {num * 7}")
print(f"{num} * 3 = {num * 3:<5} {num} * 8 = {num * 8}")
print(f"{num} * 4 = {num * 4:<5} {num} * 9 = {num * 9}")
print(f"{num} * 5 = {num * 5:<5} {num} * 10 = {num * 10}")
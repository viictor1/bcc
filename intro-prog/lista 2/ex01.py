"""
1. Escreva um programa que lê um número inteiro e informa:
a. Caractere na tabela ASCII (%c);
b. Valor decimal (%d);
c. Valor octal (%o);
d. Valor hexadecimal (%X).
"""

num = int(input("Digite um numero: "))
